let comment_region frame =
  let buf = frame.frm_buffer in
  let point = frame.frm_point in
  let text = buf.buf_text in
  let len = String.length comment_string in
  let mark = 
    match buf.buf_mark with None -> failwith "Mark is not set"
    | Some p ->  p in
