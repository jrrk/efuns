let modifiers mods =
  let m = ref 0 in
  for i = 0 to String.length mods - 1 do
    m := !m lor (match mods.[i] with
        'S' -> shiftMask
      | 'C' -> controlMask
      | 'M' -> mod1Mask
      | 'A' -> anyModifier
      | _ -> 0)
  done; !m

